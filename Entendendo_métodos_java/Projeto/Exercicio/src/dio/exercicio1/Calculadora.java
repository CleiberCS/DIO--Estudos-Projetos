package dio.exercicio1;

public class Calculadora {
    public static float somar(float valor1,float valor2){
        return valor1 + valor2;
    }

    public static float subtrair(float primeiroValor, float segundoValor) {
        return primeiroValor - segundoValor;
    }

    public static float multiplica(float primeiroValor,float segundoValor){
        return primeiroValor * segundoValor;
    }

    public static float dividir(float primeiroValor,float segundoValor){
        return primeiroValor/segundoValor;
    }
}
